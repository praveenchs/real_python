# Functions Organize the code into useful block performing specific task so that it becomes easy to understand and reuse . 
def my_function():
    print("My first function")

# passing arguments to a function
def my_func_param(arg1,arg2):
    print("Hey %s, Welcome to %s" %(arg1,arg2))

# Function may return a value to the caller , using the keyword "return"
def my_func_return(arg1,arg2):
    return arg1+arg2



# function call in Python


# simple function call
my_function()

# function call with arguments
my_func_param("Praveen","Google")

# function call which returns a value 
result = my_func_return(23,7)
print(result)

